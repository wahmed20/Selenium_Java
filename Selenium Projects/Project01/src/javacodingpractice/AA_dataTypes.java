package javacodingpractice;

public class AA_dataTypes {

	public static void main(String[] args) 
	{
		// Primitive data types: int, double, boolean, char
	// Data types
// 1. integer 
		int a = 10;
// 2. Double
		double b = 12.34;
// 3. char. only single digit value. should be written within single quotes.
		char c = 'd';
// 4. boolean 
		boolean b1 = true;
		boolean b2 = false;
// 5. String. is a class. not a data type but can be used as data type for string values
		// it should be written in double quotes
		String s = "Hello world";
	}

}
